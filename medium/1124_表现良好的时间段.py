# 给你一份工作时间表hours，上面记录着某一位员工每天的工作小时数。
# 
# 我们认为当员工一天中的工作小时数大于8 小时的时候，那么这一天就是「劳累的一天」。
# 
# 所谓「表现良好的时间段」，意味在这段时间内，「劳累的天数」是严格 大于「不劳累的天数」。
# 
# 请你返回「表现良好时间段」的最大长度。
# 
# 
# 
# 示例 1：
# 
# 输入：hours = [9,9,6,0,6,6,9]
# 输出：3
# 解释：最长的表现良好时间段是 [9,9,6]。
# 示例 2：
# 
# 输入：hours = [6,6,6]
# 输出：0
# 
# 
# 提示：
# 
# 1 <= hours.length <= 104
# 0 <= hours[i] <= 16
from typing import List


# class Solution:
#     def longestWPI(self, hours: List[int]) -> int:
#         len_L = len(hours)
#         max_len = 0
#         nums_ = [1 if hour > 8 else -1 for hour in hours]
#         pre_sum = [0]
#         for i in range(1, len(nums_) + 1):
#             pre_sum.append(pre_sum[i - 1] + nums_[i - 1])
#         for i in range(len_L + 1):
#             for j in range(len_L, i - 1, -1):
#                 if pre_sum[j] - pre_sum[i] > 0:
#                     max_len = max(max_len, j - i)
#
#         return max_len

class Solution:
    def longestWPI(self, hours: List[int]) -> int:
        len_L = len(hours)
        max_len = 0
        nums_ = [1 if hour > 8 else -1 for hour in hours]
        pre_sum = [0]
        for i in range(1, len(nums_) + 1):
            pre_sum.append(pre_sum[i - 1] + nums_[i - 1])

        ress = [] # 单调栈，多个上坡的坡底
        for left in range(len_L + 1):
            if left == 0:
                ress.append(0)
            elif pre_sum[left] < pre_sum[ress[-1]]:
                ress.append(left)

        for right in range(len_L, -1, -1):
            while ress and pre_sum[right] > pre_sum[ress[-1]]:
                max_len = max(max_len, right - ress[-1])
                ress.pop(-1)

        return max_len

